# โปรแกรมคำนวณพื้นที่และปริมาตรเรขาคณิต (เวอร์ชันดัดแปลง)
print("--- โปรแกรมคำนวณพื้นที่และปริมาตร ---")

# 1. รับค่าจากผู้ใช้ (ดัดแปลงจากการล็อกตัวเลขตายตัว)
width = float(input("กรุณากรอกความกว้าง (ซม.): "))
length = float(input("กรุณากรอกความยาว (ซม.): "))
height = float(input("กรุณากรอกความสูง (ซม.) [สำหรับหาปริมาตร]: "))

# 2. คำนวณผลลัพธ์
area = width * length
volume = width * length * height

# 3. แสดงผลลัพธ์
print("---------------------------------")
print(f"พื้นที่สี่เหลี่ยมผืนผ้า = {area} ตารางเซนติเมตร")
print(f"ปริมาตรทรงลูกบาศก์ = {volume} ลูกบาศก์เซนติเมตร")
print("---------------------------------")